## Python tic tac toe

def tic_tac_toe(board):
    """
    :type board: List[List[str]]
    :rtype: None Do not return anything, modify board in-place instead.
    """
    for i in range(3):
        for j in range(3):
            if board[i][j] == 'X':
                board[i][j] = 'O'
            elif board[i][j] == 'O':
                board[i][j] = 'X'
    return board

